/**
 * Get a translation string for the given language and key.
 * Supports placeholder substitution: {count}, {label}, etc.
 */
export function t(lang: string, key: string, params?: Record<string, string | number>): string {
  // Normalize lang: "zh-CN" → "zh-CN", "en" → "en", "ja" → "ja"
  const normalizedLang = (lang || "en") as Lang;
  const dict = translations[normalizedLang] || translations["en"];
  let str = dict[key] || translations["en"][key] || key;

  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }

  return str;
}
